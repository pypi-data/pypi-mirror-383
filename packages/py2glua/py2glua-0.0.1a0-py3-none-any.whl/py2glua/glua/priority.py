from enum import IntEnum


class Priority(IntEnum):
    """Приоритет порядки загрузки файлов
    В случае если приоритеты одинаковы - порядок выбирается через сортировку имени по алфавиту

    `HIGH` - загружается в первую очередь

    `NORMAL` - загружается в нормальном приоритете

    `LOW` - загружается в последнюю очередь
    """

    HIGH = 0
    NORMAL = 1
    LOW = 2

    def __str__(self) -> str:
        return self.name.lower()
